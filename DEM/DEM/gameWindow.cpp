#include "GameWindow.h"

GameWindow::GameWindow() : Window()
{
}

GameWindow::~GameWindow()
{
}

void GameWindow::Render(sf::RenderWindow &window)
{
	window.draw(this->m_box);

	// -- Get player info --

	// Change to get map
	char map[90][60] = { "" };
	string tempText = "";

	for (int i = 0; i < 90; i++)
	{
		for (int j = 0; j < 60; j++)
		{
			// -- IF NOT PLAYER
			tempText += map[i][j];
		}

		tempText += "\n";
	}

	this->m_text.setString(tempText);
	window.draw(this->m_text);
}

